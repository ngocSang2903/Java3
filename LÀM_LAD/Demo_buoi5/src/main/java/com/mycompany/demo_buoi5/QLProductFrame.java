    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        if (row == -1) {
            return;
        }
        String id = this.table.getValueAt(row, 0).toString();
        String tenSP = this.table.getValueAt(row, 1).toString();
        String mauSac = this.table.getValueAt(row, 2).toString();
        String soLg = this.table.getValueAt(row, 3).toString();
        String donGia = this.table.getValueAt(row, 4).toString();
        String danhMuc = this.table.getValueAt(row, 5).toString();

        this.lblId.setText(id);
        this.txttenSP.setText(tenSP);
        this.cbbMauSac.setSelectedItem(mauSac);
        this.txtSoLg.setText(soLg);
        this.txtDonGia.setText(donGia);
        this.txtDanhMucId.setText(danhMuc);
    }//GEN-LAST:event_tableMouseClicked

