    private void butupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butupdateActionPerformed
        // TODO add your handling code here:
        if(txtma.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Nhập mã sinh viên");
            txtma.requestFocus();
            return;
        }
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con =DriverManager.getConnection(url,userName,password);
            String sql="update Student set HoTen=?,Email=?,sdt=?,GioiTinh=?,DiaChi=? where MaSV=?";
            PreparedStatement st=con.prepareStatement(sql);
            st.setString(1, txtten.getText());
            st.setString(2, txtemail.getText());
            st.setString(3,txtsodt.getText());
            String gt;
            if(ranam.isSelected()){
                gt="Nam";   
            }else{
                gt="Nữ";
            }
            st.setString(4, gt);
            st.setString(5, txtdiachi.getText());
            st.setString(6, txtma.getText());
            st.executeUpdate();
            JOptionPane.showMessageDialog(this, "UPdate thành công");
            con.close();
            LoadDataToArray();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_butupdateActionPerformed

