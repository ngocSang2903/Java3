    public void display(int i) {
        Student st = list.get(i);
        txtma.setText(st.getMasv());;
        txtten.setText(st.getTen());
        txtemail.setText(st.getEmail());
        txtsodt.setText(st.getSdt() + "");
        if (st.getGioitinh().equals("Nam")) {
            ranam.setSelected(true);
        } else {
            ranu.setSelected(true);
        }
        txtdiachi.setText(st.getDiachi());
    }

    public void LoadDataToArray() {
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(url, userName, password);
            Statement st = con.createStatement();
            String sql = "Select * from Student";
            ResultSet rs = st.executeQuery(sql);
            list.clear();
            while (rs.next()) {
                String masv = rs.getString(1);
                String ten = rs.getString(2);
                String email = rs.getString(3);
                int sodt = rs.getInt(4);
                String gioitinh = rs.getString(5);
                String diachi = rs.getString(6);
                Student sv = new Student(masv, ten, email, sodt, gioitinh, diachi);
                list.add(sv);
            }
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

