    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        BanModel banModel = this.getFormData();
        if (banModel == null) {
            return;
        }
        this.banService.update(banModel.getMa(), banModel);
        this.addTable();
        JOptionPane.showMessageDialog(this, "Sua thanh cong");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        if (row == -1) {
            return;
        }

        lblma.setText((String) table.getValueAt(row, 0));
        txtten.setText((String) table.getValueAt(row, 1));
        txtsothich.setText((String) table.getValueAt(row, 2));
        if (table.getValueAt(row, 3).equals(0)) {
            rdnam.setSelected(true);
        } else {
            rdnu.setSelected(true);
        }
    }//GEN-LAST:event_tableMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int Row = table.getSelectedRow();
        if (Row == -1) {
            JOptionPane.showMessageDialog(this, "Chua chon dong");
            return;
        }
        int hoi = JOptionPane.showConfirmDialog(this, "Ban co muon xoa");
        if (hoi != JOptionPane.YES_NO_OPTION) {
            return;
        }
        String xoa = table.getValueAt(Row, 0).toString();
        this.banService.delete(xoa);
        this.addTable();
        JOptionPane.showMessageDialog(this, "Xoa thanh cong");
    }//GEN-LAST:event_jButton2ActionPerformed

