    private void addTable() {
        DefaultTableModel bang = (DefaultTableModel) table.getModel();
        this.banService.all();
        bang.setRowCount(0);
        for (BanModel banModel : this.banService.all()) {
            bang.addRow(new Object[]{banModel.getMa(), banModel.getTen(), banModel.getSothich(), banModel.getGioitinh()==0 ? "Nam" : "Ná»¯"});
        }
    }

    private BanModel getFormData() {
        String ma = lblma.getText();
        String ten = txtten.getText();
        String sothich = txtsothich.getText();
        int gioitinh;
        if (rdnam.isSelected()) {
            gioitinh = 0;
        } else {
            gioitinh = 1;
        }
        return new BanModel(ma, ten, sothich, gioitinh);
    }

