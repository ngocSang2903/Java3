    private SinhVien getFormData() {
        String maSV = this.txtma.getText();
        String tenSV = this.txtten.getText();
        String email = this.txtemail.getText();
        String soDT = this.txtsdt.getText();
        String gioitinh;
        if (ranam.isSelected()) {
            gioitinh = "Nam";
        } else {
            gioitinh = "Nữ";
        }
        String diaChi = this.txtdiachi.getText();
        if (maSV.length() == 0
                || tenSV.length() == 0
                || email.length() == 0
                || soDT.length() == 0
                || diaChi.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không được để trống.");
            return null;
        }
        this.QLSVService.getlist();
        for (SinhVien sinhvien : QLSVService.getlist()) {
            if(maSV.equals(sinhvien.getMasv())){
                JOptionPane.showMessageDialog(this, "Mã sinh viên đã tồn tại.");
                return null;
            }
            if(email.equals(sinhvien.getEmail())){
                JOptionPane.showMessageDialog(this, "Email đã tồn tại");
                return null;
            }
        }
        if (!email.matches("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
                + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$")) {
            JOptionPane.showMessageDialog(this, "Email không đúng định dang.");
            return null;
        }
        int sodt = -1;
        try {
            sodt = Integer.parseInt(soDT);
            if (sodt < 0) {
                JOptionPane.showMessageDialog(this, "Số điện thoại phải lớn hơn không");
                return null;
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Số điện thoại phải là số.");
            return null;
        }

        //load hinh anh
        String hinh;
        if (strhinhanh == null) {
            hinh = "NO AVATAR";
        } else {
            hinh = strhinhanh;
        }
        return new SinhVien(maSV, tenSV, email, sodt, gioitinh, diaChi, hinh);
    }

