    private void exportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportActionPerformed
        // TODO add your handling code here:
        File excelFile;
        FileInputStream excelInputStream = null;
        BufferedInputStream excelBIS = null;
        XSSFWorkbook excelJtableImport = null;

        String defaultCurrentDirectoryPath = "";
        JFileChooser excelFileChooser = new JFileChooser(defaultCurrentDirectoryPath);
        int excelchooser = excelFileChooser.showOpenDialog(null);

        if (excelchooser == JFileChooser.APPROVE_OPTION) {
            try {
                excelFile = excelFileChooser.getSelectedFile();
                excelInputStream = new FileInputStream(excelFile);

                excelJtableImport = new XSSFWorkbook(excelInputStream);
                XSSFSheet excelSheet = excelJtableImport.getSheetAt(0);
                Row row;
                for (int i = 1; i <= excelSheet.getLastRowNum(); i++) {
                    row = excelSheet.getRow(i);
                    String ma = row.getCell(0).getStringCellValue();
                    String hoten = row.getCell(1).getStringCellValue();
                    String email = row.getCell(2).getStringCellValue();
                    int sodt = (int) row.getCell(3).getNumericCellValue();
                    String Gioitinh = row.getCell(4).getStringCellValue();
                    String diachi = row.getCell(5).getStringCellValue();
                    String hinh = row.getCell(6).getStringCellValue();
                    this.QLSVService.insertexcel(ma, hoten, email, sodt, Gioitinh, diachi, hinh);
                }
                
                this.loadTable();
                JOptionPane.showMessageDialog(this, "Load File Excel thành công.");
            } catch (Exception e) {
                e.printStackTrace();
            }
            
        }

    }//GEN-LAST:event_exportActionPerformed
