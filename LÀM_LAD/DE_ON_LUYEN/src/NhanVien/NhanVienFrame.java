    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        lblid.setText(table.getValueAt(row, 0).toString());
        txtten.setText(table.getValueAt(row, 1).toString());
        txtma.setText(table.getValueAt(row, 2).toString());
        txtsdt.setText(table.getValueAt(row, 3).toString());
        txttuoi.setText(table.getValueAt(row, 4).toString());
//        cbbvaitro.setSelectedItem(table.getValueAt(row, 5).toString());
        if(table.getValueAt(row, 5).equals(0)){
            cbbvaitro.setSelectedIndex(0);
        }else{
            cbbvaitro.setSelectedIndex(1);
        }

    }//GEN-LAST:event_tableMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        NhanVienModel nhanVienModel = this.getFormData();
        if (nhanVienModel == null) {
            return;
        }
        this.nhanVienService.insert(nhanVienModel);
        this.LoadTable();
        this.Clear();
        JOptionPane.showMessageDialog(this, "ThemThanh cong");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        NhanVienModel nhanVienModel = this.getFormData();
        if (nhanVienModel == null) {
            return;
        }
        this.nhanVienService.update(nhanVienModel.getId(), nhanVienModel);
        this.LoadTable();
        this.Clear();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chua chon dong");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "Xác nhận xóa?");

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        String xoa = table.getValueAt(row, 0).toString();
        int id = Integer.parseInt(xoa);
        this.nhanVienService.delete(id);
        this.LoadTable();
        this.Clear();
    }//GEN-LAST:event_jButton2ActionPerformed

