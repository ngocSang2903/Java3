    private void LoadTable() {
        DefaultTableModel bang = (DefaultTableModel) table.getModel();
        this.nhanVienService.all();
        bang.setRowCount(0);
        for (NhanVienModel nhanvien : nhanVienService.all()) {
            bang.addRow(new Object[]{nhanvien.getId(), nhanvien.getTen(), nhanvien.getMa(), nhanvien.getSodt(), nhanvien.getTuoi(), nhanvien.getVaitro()==0 ? "Quản lý":"Nhân viên"});
        }
    }

    private NhanVienModel getFormData() {
        String id = lblid.getText();
        String ten = txtten.getText();
        String ma = txtma.getText();
        String sodt = txtsdt.getText();
        String tuoi = txttuoi.getText();
        int vaitro;

        if (cbbvaitro.getSelectedIndex() == 0) {
            vaitro = 0;
        } else {
            vaitro = 1;
        }
        int ID = id.equals("_") ? 0 : Integer.parseInt(id);

        if (ten.length() == 0
                || ten.length() == 0
                || ten.length() == 0
                || ten.length() == 0) {
            JOptionPane.showMessageDialog(this, "khong de trong");
            return null;
        }
        int Tuoi = -1;
        try {
            Tuoi = Integer.parseInt(tuoi);
            if (Tuoi < 0) {
                JOptionPane.showMessageDialog(this, "Tuoi khog nho hon khong");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "tuoi phai la so");
            return null;
        }

        return new NhanVienModel(ID, ten, ma, sodt, Tuoi, vaitro);
    }

